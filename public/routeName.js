function getBreadcrumbs(pageType, route, data) {
    // Создаём массив для хлебных крошек
    const crumbs = [];
    crumbs.push({
        title: "Главная",
        url: "/",
    });
    switch (pageType) {
        case "category":
            crumbs.push({
                title: data.cName,
                url: `/category/${data.cSlug}`,
            });
            break;
        case "product":
            crumbs.push({
                title: data.category.cName,
                url: `/category/${data.category.cSlug}`,
            });
            crumbs.push({
                title: data.pName,
                url: `/product/${data.pSlug}`,
            });

            break;

        default:
            break;
    }
}